function priceLabel(id: string): string {
  const pkg = REPAIR_PACKAGES.find((p) => p.id === id);
  if (!pkg) return "";
  return `от ${pkg.priceFrom.toLocaleString("ru-RU").replace(/\s/g, "\u00A0")} ₽/${pkg.unit}`;
}

