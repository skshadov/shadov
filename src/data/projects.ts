function buildAll(): HouseProject[] {
  const list: HouseProject[] = [];
  let n = 1;
  for (const m of MATERIAL_ORDER) {
    const plans = MATERIAL_PLANS[m];
    let inst = 0;
    for (const plan of plans) {
      for (let i = 0; i < plan.count; i++) {
        list.push(buildOne(n, m, plan, inst));
        n++;
        inst++;
      }
    }
  }
  return list;
}

export const PROJECTS: HouseProject[] = buildAll();

export function getProject(slug: string): HouseProject | undefined {
  return PROJECTS.find((p) => p.slug.toLowerCase() === slug.toLowerCase());
}

export function getSimilar(p: HouseProject, limit = 4): HouseProject[] {
  return PROJECTS
    .filter((x) => x.slug !== p.slug && x.primaryMaterial === p.primaryMaterial)
    .map((x) => ({ x, d: Math.abs(x.area - p.area) }))
    .sort((a, b) => a.d - b.d)
    .slice(0, limit)
    .map(({ x }) => x);
}